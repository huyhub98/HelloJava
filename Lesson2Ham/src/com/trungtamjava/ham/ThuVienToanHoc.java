package com.trungtamjava.ham;

public class ThuVienToanHoc {

	// tong
	public static int tong2So(int x, int y) {
		return x + y;
	}

	public static double tong2So(double x, double y) {
		return x + y;
	}

	// hieu
	public static int hieu2So(int x, int y) {
		return x - y;
	}

	public static double hieu2So(double x, double y) {
		return x - y;
	}

	// tich
	public static int tich2So(int x, int y) {
		return x * y;
	}

	public static double tich2So(double x, double y) {
		return x * y;
	}

	// thuong
	public static int thuong2So(int x, int y) {
		return x / y;
	}

	public static double thuong2So(double x, double y) {
		return x / y;
	}
}
