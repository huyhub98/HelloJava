package toanhoc;

public class ToanHoc {

	public static void main(String[] args) {
		int x = 10;
		long y = 100l;
		double k = 4.5;
		// Tong:
		System.out.println(x + y);
		System.out.println(x + k);
		System.out.println(y + k);

		// Tich
		System.out.println(x * y);
		System.out.println(x * k);
		System.out.println(y * k);

		// Thuong
		System.out.println(y / x);
		System.out.println(y / k);
		System.out.println(x / k);
	}

}
